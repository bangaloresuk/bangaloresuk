const TODAY_STR = '2026-06-10'

export function buildDevotees() {
  const map = {}
  RAW_BOOKINGS.forEach(([date, name, mobile]) => {
    if (!map[mobile]) map[mobile] = { name: name.trim(), mobile, dates: [] }
    if (!map[mobile].dates.includes(date)) map[mobile].dates.push(date)
    map[mobile].name = name.trim()
  })
  const TODAY = new Date(TODAY_STR)
  return Object.values(map).map(d => {
    d.dates.sort()
    d.total = d.dates.length
    const past = d.dates.filter(x => new Date(x) <= TODAY)
    d.pastCount = past.length
    d.lastPast = past.length ? past[past.length - 1] : d.dates[0]
    d.daysSince = Math.round((TODAY - new Date(d.lastPast)) / 86400000)
    d.status = d.daysSince <= 30 ? 'active' : d.daysSince <= 90 ? 'at-risk' : 'inactive'
    d.score = Math.min(100, Math.round(d.pastCount * 5 + Math.max(0, 30 - d.daysSince) * 0.8))
    return d
  }).sort((a, b) => b.total - a.total)
}

